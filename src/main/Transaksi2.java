    private void txtpcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpcodeKeyPressed
        // TODO add your handling code here:
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
            
        {
            String pcode = txtpcode.getText();
            
            try {
                Connection con = dbkoneksi.konfig.sambung();
                String sql = ("select * from data_produk where kode_produk = ?");
                PreparedStatement pst = con.prepareStatement(sql);
                
                pst.setString(1, pcode);
                rs = pst.executeQuery();
                
                if(rs.next() == false)
                {
                    JOptionPane.showMessageDialog(this, "Produk tidak ditemukan");
                }
                else
                {
                    String pname = rs.getString("nama_produk");
                    String price = rs.getString("harga_jual");
                    
                    
                    txtpname.setText(pname.trim());
                    txtprice.setText(price.trim());
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_txtpcodeKeyPressed

