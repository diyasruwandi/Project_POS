    public void Balance()
    {
        try {
             String totalText = txttotal.getText();
             double total = 0;

        // Verifikasi nilai yang diambil dari txttotal
          if (totalText != null && !totalText.isEmpty()) {
            // Ganti koma dengan titik jika ada
            total = Double.parseDouble(totalText.replace(',', '.'));
            System.out.println("Total: " + total);  // Debug output
        } else {
            System.out.println("Total kosong atau tidak valid");
            txtjujul.setText("0.00");
            return;
        }

        // Mengambil nilai bayar dari txtpay (pastikan bertipe double)
        String payText = txtpay.getText();
        double pay = 0;
        
        // Verifikasi nilai yang diambil dari txtpay
          if (payText != null && !payText.isEmpty()) {
            // Ganti koma dengan titik jika ada
            pay = Double.parseDouble(payText.replace(',', '.'));
            System.out.println("Pay: " + pay);  // Debug output
        } else {
            System.out.println("Bayar kosong atau tidak valid");
            txtjujul.setText("0.00");
            return;
        }
          
          double bal = pay - total;
          
          System.out.println("Balance: " + bal);  // Debug output
          txtjujul.setText(String.format("%.2f", bal));

        } catch (NumberFormatException e) {
            
            System.out.println("Error: " + e.getMessage());
            txtjujul.setText("0.00");
        }
    }
    
    private void updateTotal() {
    // Mendapatkan DefaultTableModel dari jTable1
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

    // Variabel untuk menghitung total harga
    double sum = 0;

    // Iterasi melalui setiap baris di tabel
    for (int i = 0; i < model.getRowCount(); i++) {
        try {
            // Ambil nilai dari kolom "Jumlah Harga" (misalnya kolom ke-4)
            String jumlahText = model.getValueAt(i, 4).toString();
            jumlahText = jumlahText.replace(',', '.');  // Menangani format angka

            // Menambahkan jumlah harga ke total
            sum += Double.parseDouble(jumlahText);
        } catch (NumberFormatException e) {
            // Jika ada kesalahan dalam format angka, abaikan baris ini
            System.out.println("Error parsing value: " + model.getValueAt(i, 4).toString());
        }
    }

    // Update txttotal dengan total yang baru (dengan format 2 desimal)
    txttotal.setText(String.format("%.2f", sum));
}
    
    
    
